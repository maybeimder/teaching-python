# Idee un programa para calcular el promedio de un estudiante dadas 4 notas

nota1 = float(input("Ingrese nota 1: "))
nota2 = float(input("Ingrese nota 2: "))
nota3 = float(input("Ingrese nota 3: "))
nota4 = float(input("Ingrese nota 4: "))

suma_notas = nota1 + nota2 + nota3 + nota4
promedio = suma_notas / 4

print("El promedio del estudiante es: ", promedio)